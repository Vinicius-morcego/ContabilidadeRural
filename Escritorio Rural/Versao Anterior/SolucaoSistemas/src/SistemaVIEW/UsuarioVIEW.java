    private void btnSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSalvarMouseClicked
        pegaSenha = txtSenha.getPassword();
        senha = String.valueOf(pegaSenha);
        if (txtLogin.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher o campo login!", "ATENÇÃO",
                    JOptionPane.WARNING_MESSAGE);
            txtLogin.requestFocusInWindow();
        } else if (senha.equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher o campo senha!", "ATENÇÃO",
                    JOptionPane.WARNING_MESSAGE);
            txtSenha.requestFocusInWindow();
        } else {
            if (txtCodigo.getText().equals("")) {
                CadastrarUsuarioVIEW();
            } else {
                AlterarUsuarioVIEW();
            }
            ConsultarUsuarioVIEW();
            UtilVIEW.LimparCampos(jPanel1);
            txtLogin.requestFocusInWindow();
        }
    }//GEN-LAST:event_btnSalvarMouseClicked

