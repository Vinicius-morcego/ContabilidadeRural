    private void tblUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsuarioMouseClicked
        if (evt.getClickCount() == 2) {
            txtCodigo.setText(tblUsuario.getValueAt(tblUsuario.getSelectedRow(), 0).toString());
            txtLogin.setText(tblUsuario.getValueAt(tblUsuario.getSelectedRow(), 1).toString());
            txtLogin.requestFocusInWindow();
            tblUsuario.clearSelection();
        }
    }//GEN-LAST:event_tblUsuarioMouseClicked

    public void CadastrarUsuarioVIEW() {
        try {
            UsuarioCTR objusuarioCTR = new UsuarioCTR();
            if (objusuarioCTR.CadastrarUsuarioCTR(txtLogin.getText(), senha)) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso!", "MENSAGEM",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar o usuario!", "ERRO",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public void AlterarUsuarioVIEW() {
        try {
            UsuarioCTR objusuarioCTR = new UsuarioCTR();
            if (objusuarioCTR.AlterarUsuarioCTR(txtLogin.getText(),
                    senha, txtCodigo.getText())) {
                JOptionPane.showMessageDialog(null, "Atualizado com sucesso!", "MENSAGEM",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao alterar o usuario!", "ERRO",
                    JOptionPane.INFORMATION_MESSAGE);
        }

    }

    private void ConsultarUsuarioVIEW() {
        try {
            UsuarioCTR objusuarioCTR = new UsuarioCTR();
            ResultSet rs = null;
            rs = objusuarioCTR.ConsultarLoginCTR();
            RestaurarUsuarioVIEW(rs);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao consultar o usuario!", "ERRO",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }

    public void RestaurarUsuarioVIEW(ResultSet rs) {
        try {
            DefaultTableModel dtm = (DefaultTableModel) tblUsuario.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                dtm.addRow(new Object[]{rs.getString("codigo"), rs.getString("login"),
                    rs.getString("data"), rs.getString("hora")});
            }
            tblUsuario.setModel(dtm);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao restaurar o usuario!", "ERRO",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }

